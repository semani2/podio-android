public class ShareFileUser {
    private final String access_token = null;
    private final String subdomain = null;
    private final String apicp = null;
    private final String appcp = null;
    private final String refresh_token = null;

    public String getAccess_token() {
        return access_token;
    }

    public String getSubdomain() {
        return subdomain;
    }

    public String getApicp() {
        return apicp;
    }

    public String getAppcp() {
        return appcp;
    }

    public String getRefresh_token() {
        return refresh_token;
    }
}
